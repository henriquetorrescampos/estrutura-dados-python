comprimento_caixa = float(input("Digite o comprimento da caixa: "))

altura_caixa = float(input("Digite o altura da caixa: "))

largura_caixa = float(input("Digite o largura da caixa: "))

volume_agua = 1000 * (comprimento_caixa * altura_caixa * largura_caixa)

print(f"A capacidade de água que pode ser armazenado na caixa é de {volume_agua} litros.")